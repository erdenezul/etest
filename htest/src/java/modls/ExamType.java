package modls;


public class ExamType {

    private long id;

    private String desc;

    public ExamType () {
    }

    public String getDesc () {
        return desc;
    }

    public void setDesc (String val) {
        this.desc = val;
    }

    public long getId () {
        return id;
    }

    public void setId (long val) {
        this.id = val;
    }

}

